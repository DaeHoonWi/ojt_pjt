public class QnaDTO {

	/** 글 번호 */
	private String q_num;

	/** ID */
	private String m_id;

	/** 제목 */
	private String q_title;
	
	/** 카테고리 */
	private String q_category;
	
	/** 조회수 */
	private int q_hit_count;
	
	/** 내용 */
	private String content;

	/** 원본 파일 이름 */
	private String q_original_file_name;

	/** 변경된 파일 이름 */
	private String q_stored_file_name;

	/** 부모 글 번호 */
	private String q_parent;
	
	/** 계층그룹 번호 */
	private String q_group_num;

	public String getQ_num() {
		return q_num;
	}

	public void setQ_num(String q_num) {
		this.q_num = q_num;
	}

	public String getM_id() {
		return m_id;
	}

	public void setM_id(String m_id) {
		this.m_id = m_id;
	}

	public String getQ_title() {
		return q_title;
	}

	public void setQ_title(String q_title) {
		this.q_title = q_title;
	}

	public String getQ_category() {
		return q_category;
	}

	public void setQ_category(String q_category) {
		this.q_category = q_category;
	}

	public int getQ_hit_count() {
		return q_hit_count;
	}

	public void setQ_hit_count(int q_hit_count) {
		this.q_hit_count = q_hit_count;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getQ_original_file_name() {
		return q_original_file_name;
	}

	public void setQ_original_file_name(String q_original_file_name) {
		this.q_original_file_name = q_original_file_name;
	}

	public String getQ_stored_file_name() {
		return q_stored_file_name;
	}

	public void setQ_stored_file_name(String q_stored_file_name) {
		this.q_stored_file_name = q_stored_file_name;
	}

	public String getQ_parent() {
		return q_parent;
	}

	public void setQ_parent(String q_parent) {
		this.q_parent = q_parent;
	}

	public String getQ_group_num() {
		return q_group_num;
	}

	public void setQ_group_num(String q_group_num) {
		this.q_group_num = q_group_num;
	}
	
	

}//End Of Class
